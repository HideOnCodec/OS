//time_slice 정의
#define TIME_SLICE 10000000
#define NULL ((void *)0)

int weight = 1;//weight는 1부터 차례대로 부여

struct {
  struct spinlock lock;
  struct proc proc[NPROC];

  long long min_priority; //우선순위 최솟값

}ptable;

static struct proc *initproc;

int nextpid = 1;
extern void forkret(void);
extern void trapret(void);

static void wakeup1(void *chan);

//SSU_Scheduler 구현
//SSU_Scheduler는 다음 실행될 프로세스로 RUNNABLE 상태 && 우선순위가 가장 낮은 프로세스를 선택하는 기법 
struct proc *ssu_schedule()
{
	struct proc *p;
	struct proc *ret = NULL; //ret은 NULL로 초기화
	//ptable 프로세스 중 가장 우선순위가 낮은 프로세스 탐색
	for (p = ptable.proc; p < &ptable.proc[NPROC]; p++) {
		if (p->state == RUNNABLE) {//p가 RUNNABLE 상태이고
			if (ret == NULL || ( ret->priority  > p->priority)) //ret이 아직 NULL이거나 ret의 우선순위보다 낮을 경우
				ret = p;//p 프로세스를 ret에 저장
		}
	}

//DEBUG=1 매개변수를 받으면 프로세스 정보 출력
#ifdef DEBUG
	if (ret)
		cprintf("PID: %d, NAME: %s, WEIGHT: %d, PRIORITY: %d\n", ret->pid, ret->name, ret->weight, ret->priority);
#endif
	//탐색 결과 프로세스 중 가장 우선순위가 낮은 ret을 다음 실행될 프로세스로 선정
	return ret; 
}

//매개변수로 받은 프로세스 proc의 우선순위 업데이트
void update_priority(struct proc *proc)
{
	//new_priority=old_priority + TIME_SLICE/weight
	proc->priority = (proc->priority)+(TIME_SLICE/proc->weight);
	
}

//우선순위의 최솟값을 갱신하는 함수
void update_min_priority(){
	struct proc *min = NULL; //min을 NULL로 초기화함
	struct proc *p;
	for (p = ptable.proc; p < &ptable.proc[NPROC]; p++) {
		if (p->state == RUNNABLE ) {//p 프로세스가 RUNNABLE 상태이고
			if (min == NULL || min->priority > p->priority)//min이 아직 NULL이거나 min의 우선순위보다 p의 우선순위가 낮으면
				min = p; //min 프로세스에 p 프로세스를 저장.
			}
		}
	//min에 담긴 프로세스가 우선순위의 최솟값이 됨
	if(min != NULL)//min이 NULL이 아니면
		ptable.min_priority = min->priority;//탐색이 끝나고 최종 min 프로세스의 우선순위 값을 min_priority에 넣어줌.
}

//우선순위 최솟값 부여
void assign_min_priority(struct proc *proc)
{
	// 프로세스 생성 및 wake up 시 우선순위 최솟값 부여
	// 우선순위로 0을 부여하면 해당 프로세스가 독점적으로 실행될 수 있으므로 최솟값을 부여하는 것임.
	proc->priority = ptable.min_priority;
}

void
pinit(void)
{
  initlock(&ptable.lock, "ptable");
}

// Must be called with interrupts disabled
int
cpuid() {
  return mycpu()-cpus;
}

// Must be called with interrupts disabled to avoid the caller being
// rescheduled between reading lapicid and running through the loop.
struct cpu*
mycpu(void)
{
  int apicid, i;
  
  if(readeflags()&FL_IF)
    panic("mycpu called with interrupts enabled\n");
  
  apicid = lapicid();
  // APIC IDs are not guaranteed to be contiguous. Maybe we should have
  // a reverse map, or reserve a register to store &cpus[i].
  for (i = 0; i < ncpu; ++i) {
    if (cpus[i].apicid == apicid)
      return &cpus[i];
  }
  panic("unknown apicid\n");
}

// Disable interrupts so that we are not rescheduled
// while reading proc from the cpu structure
struct proc*
myproc(void) {
  struct cpu *c;
  struct proc *p;
  pushcli();
  c = mycpu();
  p = c->proc;
  popcli();
  return p;
}

